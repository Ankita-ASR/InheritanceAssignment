public class Employee {
    private String name;
    private double baseSalary;
    public double getSalary() {
        return baseSalary;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public void setBaseSalary(double baseSalary) {
        this.baseSalary = baseSalary;
    }

}
